# Robot Framework pipe-table → structured lines conversion
# ---------------------------------------------------------------------------

def convert_pipe_table(lines):
    """
    Convert a Robot Framework pipe-style table:
    """
    out = []

    for raw in lines:
        if "|" not in raw:
            return []

        # Split & clean
        cells = [c.strip() for c in raw.split("|") if c.strip()]
        if not cells:
            continue

        out.append("    " + "    ".join(cells))

    return out

console = Console()

# ---------------------------------------------------------------------------
# List all installed pip packages, with optional filter
# ---------------------------------------------------------------------------

def list_installed_packages(filter_value=None):
    """Display pip list using Rich, with optional filtering."""
    try:
        raw = subprocess.check_output(["pip", "list"], text=True)
    except Exception as e:
        console.print(f"[red]Error: {e}[/red]")
        return

    lines = raw.splitlines()[2:]  # Skip header

    filtered_rows = []

    for line in lines:
        if not line.strip():
            continue

        parts = line.split()
        if len(parts) < 2:
            continue

        pkg, ver = parts[0], parts[1]

        # Filter
        if filter_value and filter_value.lower() not in pkg.lower():
            continue

        filtered_rows.append((pkg, ver))

    # No results
    if filter_value and not filtered_rows:
        console.print(f"[yellow]No packages match the filter:[/yellow] {filter_value}")
        return

    console.print()
    console.print("Packages installed (pip list)\n", style="bold yellow")

    table = Table(show_header=True, header_style="bold")
    table.add_column("Package", style="cyan")
    table.add_column("Version", style="green")

    for pkg, ver in filtered_rows:
        table.add_row(pkg, ver)

    console.print(table)

# ---------------------------------------------------------------------------
# List RPA libraries (RPA.*, RPA.Email, RPA.Excel, etc.)
# ---------------------------------------------------------------------------

def list_rpa_libraries():
    libs = []

    # Charge Namespace RPA
    try:
        import RPA
    except ImportError:
        return libs

    # Parse RPA.*
    for _, mod1, _ in pkgutil.iter_modules(RPA.__path__):
        parent = f"RPA.{mod1}"

        # Add parent module
        libs.append(parent)

        # Explorer Sub modules : for example RPA.Email.* 
        try:
            pkg = importlib.import_module(parent)
            if hasattr(pkg, "__path__"):
                for _, mod2, _ in pkgutil.iter_modules(pkg.__path__):
                    full = f"{parent}.{mod2}"
                    libs.append(full)
        except Exception:
            pass

    return libs

# ---------------------------------------------------------------------------
# Return True if Standard Python module
# ---------------------------------------------------------------------------

def is_stdlib_module(module):
    """
    Return True if Python standard module  (os, sys, etc...)
    """
    try:
        path = os.path.realpath(getattr(module, "__file__", ""))
        if not path:
            return True  
        
        stdlib = os.path.realpath(sysconfig.get_paths()["stdlib"])
        
        # stdlib blocked
        if path.startswith(stdlib):
            return True
        
        # rest of site-packages → not blocked
        return False
    except Exception:
        return False

# ---------------------------------------------------------------------------
# Return True if a standard python module
# ---------------------------------------------------------------------------

def is_robot_library(name):

    BUILTIN_LIBRARIES = {
    "BuiltIn",
    "Collections",
    "DateTime",
    "Dialogs",
    "OperatingSystem",
    "Process",
    "Screenshot",
    "String",
    "Telnet",
    "XML"
    }

    if name in BUILTIN_LIBRARIES:
        return True

    # libdoc detection
    try:
        libdoc = LibraryDocumentation(name)
    except Exception:
        return False
    delete_log_files()

    # Must have keywords
    # if not any(not kw.name.startswith("_") for kw in libdoc.keywords):
    #     return False

    # Dynamic / hybrid / class libraries
    if getattr(libdoc, "library_type", None) in ("DYNAMIC", "HYBRID", "CLASS"):
        return True

    # Check Python classes for robot attributes
    try:
        module = importlib.import_module(name)
        for _, cls in inspect.getmembers(module, inspect.isclass):
            if any(hasattr(cls, a) for a in (
                "ROBOT_LIBRARY_VERSION",
                "ROBOT_LIBRARY_SCOPE",
                "ROBOT_LIBRARY_DOC_FORMAT"
            )):
                return True
    except Exception:
        pass

    try:
        dist = md.distribution(name)
        pkg = dist.metadata["Name"]

        if pkg.startswith("robotframework-"):
            return True

    except Exception:
        pass

    return False



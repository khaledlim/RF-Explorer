# Code width utility for Rich Syntax formatting
# ---------------------------------------------------------------------------

def compute_code_width(lines, clamp=(20, 160)):
    """
    Compute a suitable width for a code block based on its longest line.

    Args:
        lines (list[str]): Lines of code.
        clamp (tuple): (min_width, max_width)

    Returns:
        int: Best-fit width.
    """
    max_len = max((len(l) for l in lines if l), default=0)
    width = max(clamp[0], min(clamp[1], max_len + 2))
    return width


# ---------------------------------------------------------------------------
# Docstring cleanup: remove inline "Description:" prefix
# ---------------------------------------------------------------------------

def strip_inline_description(doc_text: str) -> str:
    """
    Some Robot Framework libraries place:
        'Description: text...'
    on the same line. This causes duplicate Description sections.

    This function removes that inline prefix only.
    """

    if not doc_text:
        return doc_text

    first_line = doc_text.lstrip().split("\n", 1)[0].strip().lower()

    if first_line.startswith("description:"):
        # Remove only the prefix "Description:" and keep the rest
        return doc_text.split(":", 1)[1].lstrip()

    return doc_text


# ---------------------------------------------------------------------------
# RF doc section splitter (Description / Arguments / Examples)
# ---------------------------------------------------------------------------
def split_robot_sections(doc_text: str):
    """
    Split a RobotFramework docstring into:
    - description
    - arguments
    - examples (Robot code only)
    """

    if not doc_text:
        return {"description": "", "arguments": "", "examples": ""}

    doc_text = strip_inline_description(doc_text)

    # Patterns
    examples_pattern = r"(?im)^\s*(?:examples?|exemples?)\b.*:\s*"
    arguments_pattern = r"(?im)^\s*(?:arguments?|param[eè]tres?)\b.*:\s*"

    # Split text into description and after examples
    parts = re.split(examples_pattern, doc_text, maxsplit=1)
    description_part = parts[0].strip()
    examples = parts[1].strip() if len(parts) > 1 else ""

    # Extract Arguments section inside description
    parts2 = re.split(arguments_pattern, description_part, maxsplit=1)
    description = parts2[0].strip()
    argument_details = parts2[1].strip() if len(parts2) > 1 else ""

    return {
        "description": description,
        "arguments": argument_details,
        "examples": examples,
    }

# ---------------------------------------------------------------------------
# Cleanup generated log files generated by Libdoc
# ---------------------------------------------------------------------------
def delete_log_files(log_dir="."):
    logging.shutdown()

    root = Path(log_dir)

    for f in root.rglob("*.log"):
        try:
            f.unlink()
        except:
            pass

    folders = ("output", "outputs", "libdoc", "tmp", "temp")

    for d in root.iterdir():
        if d.is_dir() and d.name.lower() in folders:
            shutil.rmtree(d, ignore_errors=True)

